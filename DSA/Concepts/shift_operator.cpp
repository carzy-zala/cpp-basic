#include <bits/stdc++.h>


using namespace std;


int main()
{
    // closed side of arrow show which side shift operation done
    cout << " 5 >> 1 : " << (5>>1) << endl;
    cout << " 5 >> 2 : " << (5>>2) << endl;
    cout << " 5 << 1 : " << (5<<1) << endl;
    cout << " 5 << 2 : " << (5<<2) << endl;
 

    return 0;
}